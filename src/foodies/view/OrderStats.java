    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int starters = 0;
        int mains = 0;
        int desserts = 0;
        int beverages = 0;
                
        for(int i=0; i<orderStats.size(); i++){
            if(orderStats.get(i).getItemType().equals("Starters")){
                starters += orderStats.get(i).getQuantity();
            }
            else if(orderStats.get(i).getItemType().equals("Mains")){
                mains += orderStats.get(i).getQuantity();
            }
            else if(orderStats.get(i).getItemType().equals("Desserts")){
                desserts += orderStats.get(i).getQuantity();
            }
            else if(orderStats.get(i).getItemType().equals("Beverages")){
                beverages += orderStats.get(i).getQuantity();
            }
        }
        DefaultCategoryDataset dcd = new DefaultCategoryDataset();
        dcd.setValue(starters, "Orders", "Starters");
        dcd.setValue(mains, "Orders", "Mains");
        dcd.setValue(desserts, "Orders", "Desserts");
        dcd.setValue(beverages, "Orders", "Beverages");
        
        JFreeChart jfree = ChartFactory.createBarChart("Orders", "Item Type", "No of Orders", dcd, PlotOrientation.VERTICAL, true, true, false);
        CategoryPlot plot = jfree.getCategoryPlot();
        plot.setRangeGridlinePaint(Color.BLACK);
        
        ChartFrame frame = new ChartFrame("Order Statistics",jfree, true);
        frame.setSize(700,500);
        frame.setLocationRelativeTo(null);
        frame.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed



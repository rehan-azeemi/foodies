    private void loadOrderStats(){
        orderStats = DbManager.getOrderStats();
        String starters = "";
        String beverages = "";
        String desserts = "";
        String main = "";
        
        for(int i=0; i<orderStats.size(); i++){
            if(orderStats.get(i).getItemType().equals("Starters")){
                starters += orderStats.get(i).getQuantity()+"  "+orderStats.get(i).getName()+"<br>";
            }
            else if(orderStats.get(i).getItemType().equals("Mains")){
                main += orderStats.get(i).getQuantity()+"  "+orderStats.get(i).getName()+"<br>";
            }
            else if(orderStats.get(i).getItemType().equals("Desserts")){
                desserts += orderStats.get(i).getQuantity()+"  "+orderStats.get(i).getName()+"<br>";
            }
            else if(orderStats.get(i).getItemType().equals("Beverages")){
                beverages += orderStats.get(i).getQuantity()+"  "+orderStats.get(i).getName()+"<br>";
            }
        }
        
        jLabel6.setText("<html>"+starters+"</html>");
        jLabel4.setText("<html>"+desserts+"</html>");
        jLabel2.setText("<html>"+main+"<html>");
        jLabel8.setText("<html>"+beverages+"<html>");
    }
    
